SELECT MCDP_CD
     , COUNT(APNT_NO) 
FROM APPOINTMENT
WHERE APNT_YMD BETWEEN '2022-05-01' AND '2022-05-31'
GROUP BY MCDP_CD
ORDER BY COUNT(APNT_NO),MCDP_CD














